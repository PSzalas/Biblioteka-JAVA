    public ListUzytkownicy(String tytul, int i) {
        initComponents();
        
        try {
            Connection con=PolaczenieBaza.getCon();
            Statement st=con.createStatement();
            String parametr = "";
            switch (i) {
                case 1:
                    parametr = "imię";
                    break;
                case 2:
                    parametr = "nazwisko";
                    break;
                case 3:
                    parametr = "numerkarty";
                    break;
                case 4:
                    parametr = "numertelefonu";
                    break;
                default:
                    break;
            }
                  
            String sql = "select * from uzytkownik where "+parametr+"='"+tytul+"'";
            ResultSet rs = st.executeQuery(sql);
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while(rs.next()){
                String tytuł = rs.getString("imię");
                String autor = rs.getString("nazwisko");
                String wydawnictwo = rs.getString("numerkarty");
                String numerkatalogowy = rs.getString("numertelefonu");
                String zablokowany = rs.getString("zablokowany");

                String tbData[]={tytuł,autor,wydawnictwo,numerkatalogowy,zablokowany};
                DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();

                tblModel.addRow(tbData);
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Wysątpił błąd");
        }
    }
    
