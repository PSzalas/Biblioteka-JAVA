    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String imię=jTextField1.getText();
        String nazwisko=jTextField2.getText();
        String numerkarty=jTextField3.getText();
        String numertelefonu=jTextField4.getText();
        int zablokowany=Integer.parseInt(jTextField5.getText());
        
        try{
            Connection con=PolaczenieBaza.getCon();
            Statement st=con.createStatement();
            st.execute("update uzytkownik set imię='"+imię+"',nazwisko='"+nazwisko+"',numertelefonu='"+numertelefonu+"',zablokowany='"+zablokowany+"' where numerkarty='"+numerkarty+"'");
            JOptionPane.showMessageDialog(null, "Pomyślnie zaktualizowano rekord bazy");
            setVisible(false);
            new Edytujuzytkownika().setVisible(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Nie można zaktualizować danych");
            setVisible(false);
            new Edytujuzytkownika().setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

